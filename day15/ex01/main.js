let nAll = [1, 2, 5, 3, 5, 6, 7, 8];
console.log(nAll);

for (let i = 0; i < nAll.length; i++) {
    if (nAll[i] % 2 == 0) {
        console.log('Even Numbers = ' + nAll[i]);
    }
    console.log(i);
}






